// Generate monthly water stress data for the last 3 months
export const generateMonthlyData = (countyId) => {
  const county = counties.find(c => c.id === countyId);
  if (!county) return [];

  const months = ['August', 'September', 'October'];
  const currentDate = new Date();
  
  return months.map((month, index) => {
    const variation = (Math.random() - 0.5) * 10;
    return {
      month,
      waterStress: Math.max(0, Math.min(100, county.waterStress + variation)),
      rainfall: Math.max(0, county.rainfall * (0.8 + Math.random() * 0.4) / 12),
      waterAccess: Math.max(0, Math.min(100, 100 - county.waterStress + (Math.random() - 0.5) * 5))
    };
  });
};

// Generate annual water stress data for scatter plot
export const getAnnualWaterStressData = () => {
  return counties.map(county => ({
    county: county.name,
    waterStress: county.waterStress,
    population: Math.floor(county.area / 10) // Simplified population estimate
  }));
};

